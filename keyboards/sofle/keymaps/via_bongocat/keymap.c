//
// Render left OLED display
//
static void render_status(void) {

    // OLD WPM Code
    oled_write_P(PSTR("      "), false);
    //sprintf(wpm_str, "%03d", get_current_wpm());
    //oled_write(wpm_str, false);
    //oled_write_P(PSTR("   WPM"), false);

    // NEW CODE
    oled_write_P(PSTR("WPM: "), false);
    oled_write(get_u8_str(get_current_wpm(), '0'), false);

    // GUI keys indicator
    if (gui_on) oled_write_P(PSTR("\n       "), false);
    else oled_write_P(PSTR("\n      GUI   OFF"), false);

    // Caps lock indicator
    led_t led_state = host_keyboard_led_state();
    oled_write_P(led_state.caps_lock ? PSTR("\n   CAPS LOCK") : PSTR("\n     "), false);

    // Layer indicator
    oled_write_P(PSTR("\n     LAYER "), false);

    switch (get_highest_layer(layer_state)) {
        case 2:
	    oled_write_P(PSTR("\nRAISE"), false);
	    break;
	// Layer 1
        case 1:
            oled_write_P(PSTR("\nLOWER"), false);
            break;
        // Layer 0
        default:
            oled_write_P(PSTR("\n BASE "), false);
            break;
    }
}

